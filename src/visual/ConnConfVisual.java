    public ConnConfVisual(java.awt.Frame parent, boolean modal) throws ClassNotFoundException, IOException {
        super(parent, modal);
        initComponents();
        setLocationRelativeTo(null);

        // Close the dialog when Esc is pressed
        String cancelName = "cancel";
        InputMap inputMap = getRootPane().getInputMap(JComponent.WHEN_ANCESTOR_OF_FOCUSED_COMPONENT);
        inputMap.put(KeyStroke.getKeyStroke(KeyEvent.VK_ESCAPE, 0), cancelName);
        ActionMap actionMap = getRootPane().getActionMap();
        actionMap.put(cancelName, new AbstractAction() {
            public void actionPerformed(ActionEvent e) {
                doClose(RET_CANCEL);
            }
        });

        ObjectInputStream ois;
        try {
            ois = new ObjectInputStream(new FileInputStream("conf"));
            //se puede cambiar por ConnectionConfiguration
            Object readObject = ois.readObject();
            if (readObject != null) {
                conf = (Conf) readObject;
                jPasswordField1.requestFocus();
            }
            jTFServidor.setText(conf.getServidor());
            jTFUsuario.setText(conf.getUsuario());
            JSPuerto.setValue(conf.getPuerto());
            jCBModo.setSelected(conf.isMensaje());

            ois.close();
        } catch (FileNotFoundException ex) {
            Logger.getLogger(ConnConfVisual.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

