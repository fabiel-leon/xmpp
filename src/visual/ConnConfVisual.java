    public ConnectionConfiguration getConnectionConfiguration() {
        //se puede cambiar por ConnectionConfiguration
        conf.setPuerto((Integer) JSPuerto.getValue());
        conf.setServidor(jTFServidor.getText());
        conf.setUsuario(jTFUsuario.getText());
        conf.setMensaje(jCBModo.isSelected());

        cc = new ConnectionConfiguration(conf.getServidor(), conf.getPuerto());
        cc.setCompressionEnabled(true);
      //  cc.setDebuggerEnabled(true);
        if (conf.isMensaje()) {
            cc.setRosterLoadedAtLogin(false);
            cc.setSendPresence(false);
            cc.setUsedHostAddress(new HostAddress("NingunSitio.uci.cu"));
        }
        return cc;
    }

